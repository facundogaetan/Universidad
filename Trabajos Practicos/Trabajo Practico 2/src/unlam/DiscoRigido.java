public class DiscoRigido {
	private double tamanio;
	private double resultado;

	//los divisores estan expresados en tb
	public double cuantosDVD(double tamanio) {
		return resultado = tamanio / 0.0045;
			}
	
	public double cuantosBD(double tamanio) {
		return resultado = tamanio / 0.015;
	}
}